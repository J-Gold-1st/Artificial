        async function webSearch(query) {
            try {
                const res = await fetch(`https://api.duckduckgo.com/?q=${encodeURIComponent(query)}&format=json&pretty=1`);
                const data = await res.json();
                if (data.Abstract) {
                    return `${data.Abstract} <br><a href="${data.AbstractURL || data.RelatedTopics?.[0]?.FirstURL || '#'}" target="_blank">Read more on DuckDuckGo</a>`;
                } else if (data.RelatedTopics?.length > 0) {
                    const topic = data.RelatedTopics[0];
                    return `${topic.Text} <br><a href="${topic.FirstURL}" target="_blank">Learn more</a>`;
                }
                return "I found some info, but couldn't extract a clear summary. Try a more specific question!";
            } catch (err) {
                return "Sorry, web search failed right now. Ask me something about Janvier instead 😅";
            }
        }

